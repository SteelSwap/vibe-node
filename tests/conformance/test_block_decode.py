# ---------------------------------------------------------------------------
# Test 6: Block hash deterministic
# ---------------------------------------------------------------------------

@pytest.mark.conformance
@pytest.mark.timeout(30)
async def test_block_hash_deterministic() -> None:
    """Verify decoding the same block twice produces the same hash.

    Catches non-determinism in CBOR re-encoding or hash computation.
    The hash must be identical across multiple decode passes.
    """
    fixtures = _load_fixtures()
    if not fixtures:
        pytest.skip(SKIP_MSG)

    for fixture in fixtures:
        cbor_bytes = bytes.fromhex(fixture["cbor"])

        header1 = decode_block_header(cbor_bytes)
        header2 = decode_block_header(cbor_bytes)

        assert header1.hash == header2.hash, (
            f"Non-deterministic hash at slot {header1.slot}: "
            f"{header1.hash.hex()} != {header2.hash.hex()}"
        )
        assert header1.header_cbor == header2.header_cbor, (
            f"Non-deterministic header CBOR at slot {header1.slot}"
        )


# ---------------------------------------------------------------------------
# Test 7: Ogmios block metadata validation (live, no CBOR needed)
# ---------------------------------------------------------------------------

@pytest.mark.conformance
@pytest.mark.timeout(120)
async def test_ogmios_block_metadata(ogmios_url: str) -> None:
    """Validate block metadata from Ogmios chain-sync (live test).

    Works without CBOR fixtures — validates Ogmios JSON responses:
    - Each block has a slot number > 0
    - Each block has a 64-char hex block hash
    - Each block has a known era name
    - Block heights are monotonically increasing
    - Transactions field is a list
    """
    blocks = await _ogmios_chain_sync_blocks(ogmios_url, max_blocks=10)
    assert len(blocks) > 0, "Expected at least 1 post-Byron block from Ogmios"

    valid_eras = {"shelley", "allegra", "mary", "alonzo", "babbage", "conway"}
    prev_height = -1

    for block in blocks:
        slot = block.get("slot")
        assert isinstance(slot, int) and slot > 0, f"Invalid slot: {slot}"

        block_id = block.get("id")
        assert isinstance(block_id, str) and len(block_id) == 64, (
            f"Invalid block hash: {block_id}"
        )
        bytes.fromhex(block_id)  # Verify valid hex

        era = (block.get("era") or "").lower()
        assert era in valid_eras, f"Unknown era '{era}' at slot {slot}"

        height = block.get("height")
        if isinstance(height, int):
            assert height > prev_height, (
                f"Heights not monotonically increasing: {height} <= {prev_height}"
            )
            prev_height = height

        txs = block.get("transactions")
        assert isinstance(txs, list), (
            f"Transactions should be a list at slot {slot}"
        )


