FIXTURE_DIR = Path(__file__).parent / "fixtures" / "blocks"

SKIP_MSG = (
    "No block CBOR fixtures found. Generate them with:\n"
    "  docker run --rm \\\n"
    "    -v vibe-node_cardano-node-ipc:/ipc:ro \\\n"
    "    -v $(pwd)/tests/conformance:/tests/conformance \\\n"
    "    -v $(pwd)/packages/vibe-cardano/src:/src \\\n"
    "    -e PYTHONPATH=/src \\\n"
    "    --network vibe-node_vibe-node \\\n"
    "    python:3.14-slim \\\n"
    '    bash -c "pip install cbor2 websockets && '
    'python3 /tests/conformance/generate_fixtures.py"'
)


def _load_fixtures() -> list[dict[str, Any]]:
    """Load block CBOR fixtures from disk.

    Each fixture is a JSON file with:
      {
        "slot": int,
        "hash": str (hex),
        "era": str,
        "height": int,
        "tx_count": int,
        "cbor": str (hex-encoded tagged block CBOR)
      }
    """
    if not FIXTURE_DIR.exists():
        return []
    fixtures = []
    for path in sorted(FIXTURE_DIR.glob("*.json")):
        with open(path) as f:
            data = json.load(f)
            if data.get("cbor"):  # Only include fixtures with CBOR
                fixtures.append(data)
    return fixtures


def _era_name_to_enum(era_name: str) -> Era | None:
    """Map Ogmios era name string to our Era enum."""
    mapping = {
        "byron": Era.BYRON_MAIN,
        "shelley": Era.SHELLEY,
        "allegra": Era.ALLEGRA,
        "mary": Era.MARY,
        "alonzo": Era.ALONZO,
        "babbage": Era.BABBAGE,
        "conway": Era.CONWAY,
    }
    return mapping.get(era_name.lower() if era_name else "")


# ---------------------------------------------------------------------------
# Ogmios chain-sync helpers
# ---------------------------------------------------------------------------

async def _ogmios_chain_sync_blocks(
    ogmios_url: str, max_blocks: int = 10, max_walk: int = 6000
) -> list[dict[str, Any]]:
    """Walk from origin via Ogmios chain-sync and collect post-Byron blocks."""
    blocks: list[dict[str, Any]] = []

    async with websockets.connect(ogmios_url) as ws:
        await ws.send(json.dumps({
            "jsonrpc": "2.0",
            "method": "findIntersection",
            "params": {"points": ["origin"]},
            "id": "sync-start",
        }))
        resp = json.loads(await ws.recv())
        if "error" in resp:
            raise RuntimeError(f"findIntersection error: {resp['error']}")

        for _ in range(max_walk):
            await ws.send(json.dumps({
                "jsonrpc": "2.0",
                "method": "nextBlock",
                "id": "next",
            }))
            resp = json.loads(await ws.recv())
            result = resp.get("result", {})
            direction = result.get("direction")

            if direction == "backward":
                continue
            elif direction == "forward":
                block = result.get("block")
                if block is not None and isinstance(block, dict):
                    era = (block.get("era") or "").lower()
                    if era != "byron":
                        blocks.append(block)
                        if len(blocks) >= max_blocks:
                            break
            else:
                break

    return blocks


async def _ogmios_chain_sync_by_era(
    ogmios_url: str, max_walk: int = 20_000
) -> dict[str, dict[str, Any]]:
    """Walk from origin and collect the first block from each post-Byron era."""
    era_blocks: dict[str, dict[str, Any]] = {}
    era_order = ["shelley", "allegra", "mary", "alonzo", "babbage", "conway"]

    async with websockets.connect(ogmios_url) as ws:
        await ws.send(json.dumps({
            "jsonrpc": "2.0",
            "method": "findIntersection",
            "params": {"points": ["origin"]},
            "id": "era-sync",
        }))
        resp = json.loads(await ws.recv())
        if "error" in resp:
            return era_blocks

        for _ in range(max_walk):
            if len(era_blocks) >= len(era_order):
                break

            await ws.send(json.dumps({
                "jsonrpc": "2.0",
                "method": "nextBlock",
                "id": "era-walk",
            }))
            resp = json.loads(await ws.recv())
            result = resp.get("result", {})

            if result.get("direction") != "forward":
                continue

            block = result.get("block")
            if block is None or not isinstance(block, dict):
                continue

            era = (block.get("era") or "").lower()
            if era in era_order and era not in era_blocks:
                era_blocks[era] = block

    return era_blocks


# ---------------------------------------------------------------------------
# Test 1: Decode recent blocks (fixture-based)
# ---------------------------------------------------------------------------

@pytest.mark.conformance
@pytest.mark.timeout(30)
async def test_decode_recent_blocks() -> None:
    """Decode block CBOR fixtures, verify valid BlockHeader objects.

    Validates that our decoder handles real chain data across eras:
    - decode_block_header returns a valid BlockHeader
    - Slot numbers match fixture metadata
    - Block numbers are positive
    - Era matches fixture metadata
    - Issuer vkey is 32 bytes (Ed25519)
    - Block body hash is 32 bytes (Blake2b-256)
    - Protocol version major >= 2 for all post-Byron blocks
    """
    fixtures = _load_fixtures()
    if not fixtures:
        pytest.skip(SKIP_MSG)

    decoded_count = 0

    for fixture in fixtures:
        cbor_bytes = bytes.fromhex(fixture["cbor"])
        expected_slot = fixture["slot"]
        expected_era = fixture.get("era", "").lower()

        header = decode_block_header(cbor_bytes)

        assert isinstance(header, BlockHeader), (
            f"Expected BlockHeader, got {type(header).__name__}"
        )
        assert header.slot == expected_slot, (
            f"Slot mismatch: decoder={header.slot}, fixture={expected_slot}"
        )
        assert header.block_number > 0, (
            f"Block number should be > 0, got {header.block_number}"
        )

        if expected_era:
            expected_enum = _era_name_to_enum(expected_era)
            if expected_enum is not None:
                assert header.era == expected_enum, (
                    f"Era mismatch at slot {expected_slot}: "
                    f"decoder={header.era.name}, fixture={expected_era}"
                )

        assert len(header.issuer_vkey) == 32, (
            f"Issuer vkey should be 32 bytes, got {len(header.issuer_vkey)}"
        )
        assert len(header.block_body_hash) == 32, (
            f"Block body hash should be 32 bytes, got {len(header.block_body_hash)}"
        )
        assert header.protocol_version.major >= 2, (
            f"Protocol major should be >= 2, got {header.protocol_version.major}"
        )

        decoded_count += 1

    assert decoded_count >= 1, f"Decoded {decoded_count} blocks"


# ---------------------------------------------------------------------------
# Test 2: Block hash matches Ogmios
# ---------------------------------------------------------------------------

@pytest.mark.conformance
@pytest.mark.timeout(30)
async def test_block_hash_matches_ogmios() -> None:
    """Compute block_hash from our decoder, compare against Ogmios-reported hash.

    The block hash is Blake2b-256 of the CBOR-encoded header. The fixture
    records the hash from Ogmios (the Haskell node's oracle truth). If our
    hash doesn't match, our header extraction or CBOR re-encoding is wrong.

    This is the CRITICAL conformance check for the block decoder.

    Spec: block hash = Blake2b-256(header_cbor) where header_cbor is the
    serialized [header_body, body_signature] array.
    """
    fixtures = _load_fixtures()
    if not fixtures:
        pytest.skip(SKIP_MSG)

    verified_count = 0

    for fixture in fixtures:
        cbor_bytes = bytes.fromhex(fixture["cbor"])
        expected_hash = fixture["hash"]

        if not expected_hash:
            continue  # No Ogmios hash available

        header = decode_block_header(cbor_bytes)
        our_hash = header.hash.hex()

        assert our_hash == expected_hash, (
            f"Block hash mismatch at slot {header.slot}!\n"
            f"  Our hash:    {our_hash}\n"
            f"  Ogmios hash: {expected_hash}\n"
            f"  Era: {header.era.name}\n"
            f"  Block number: {header.block_number}\n"
            f"  Header CBOR length: {len(header.header_cbor)} bytes"
        )

        verified_count += 1

    assert verified_count >= 1, f"Verified {verified_count} block hashes"


