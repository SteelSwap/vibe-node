@pytest.mark.conformance
@pytest.mark.timeout(600)
async def test_multi_era_ogmios(ogmios_url: str) -> None:
    """Walk Ogmios chain-sync and verify multiple eras exist on preprod.

    This live test validates that preprod has gone through multiple hard
    forks and our Ogmios chain-sync walk correctly identifies era transitions.
    Must find at least 2 different eras.
    """
    era_blocks = await _ogmios_chain_sync_by_era(ogmios_url)

    for era_name, block in era_blocks.items():
        assert block.get("era", "").lower() == era_name
        assert isinstance(block.get("slot"), int)
        assert isinstance(block.get("id"), str)

    assert len(era_blocks) >= 2, (
        f"Expected >= 2 eras from Ogmios, got: {sorted(era_blocks.keys())}"
    )
