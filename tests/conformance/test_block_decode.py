# Test 3: Multi-era decode
# ---------------------------------------------------------------------------

@pytest.mark.conformance
@pytest.mark.timeout(30)
async def test_multi_era_decode() -> None:
    """Verify blocks from different eras decode correctly.

    Validates structural differences across eras:
    - Shelley-Alonzo: two VRF certs (nonce_vrf + leader_vrf), 15+ header fields
    - Babbage-Conway: single vrf_result, 14+ header fields

    Must decode at least 2 different eras to pass.
    """
    fixtures = _load_fixtures()
    if not fixtures:
        pytest.skip(SKIP_MSG)

    eras_decoded: set[str] = set()

    for fixture in fixtures:
        cbor_bytes = bytes.fromhex(fixture["cbor"])
        expected_era = fixture.get("era", "").lower()

        header = decode_block_header(cbor_bytes)
        assert isinstance(header, BlockHeader)

        expected_enum = _era_name_to_enum(expected_era)
        if expected_enum is not None:
            assert header.era == expected_enum, (
                f"Era mismatch for {expected_era}: decoder={header.era.name}"
            )

        assert header.slot > 0
        assert header.block_number > 0
        assert len(header.issuer_vkey) == 32
        assert len(header.block_body_hash) == 32

        eras_decoded.add(header.era.name)

    assert len(eras_decoded) >= 2, (
        f"Expected >= 2 eras decoded, got: {sorted(eras_decoded)}"
    )


# ---------------------------------------------------------------------------
# Test 4: Transaction count matches
# ---------------------------------------------------------------------------

@pytest.mark.conformance
@pytest.mark.timeout(30)
async def test_transaction_count_matches() -> None:
    """Decode full block body, count transactions, compare against Ogmios.

    Block CDDL structure:
      block = [header, transaction_bodies, transaction_witness_sets, auxiliary_data_set]
      transaction_bodies = [* transaction_body]  (CBOR array)

    The fixture records the tx count from Ogmios.
    """
    from vibe.cardano.serialization.block import _strip_tag, _loads

    fixtures = _load_fixtures()
    if not fixtures:
        pytest.skip(SKIP_MSG)

    verified_count = 0

    for fixture in fixtures:
        expected_tx_count = fixture.get("tx_count")
        if expected_tx_count is None:
            continue

        cbor_bytes = bytes.fromhex(fixture["cbor"])
        _tag, payload = _strip_tag(cbor_bytes)
        block_array = _loads(payload)

        if not isinstance(block_array, list) or len(block_array) < 4:
            continue

        tx_bodies = block_array[1]

        if isinstance(tx_bodies, (list, dict)):
            our_tx_count = len(tx_bodies)
        else:
            continue

        assert our_tx_count == expected_tx_count, (
            f"TX count mismatch at slot {fixture['slot']}!\n"
            f"  Our count:    {our_tx_count}\n"
            f"  Ogmios count: {expected_tx_count}\n"
            f"  Era: {fixture.get('era')}"
        )

        verified_count += 1

    assert verified_count >= 1, f"Verified {verified_count} tx counts"


