"""Block decode conformance tests — decode + re-hash vs Ogmios.

VNODE-158: Fetch blocks from the Docker Compose Ogmios instance (connected
to a Haskell cardano-node on preprod), decode them with our block header
decoder, and verify hashes match what Ogmios reports.

Architecture:
  Block CBOR fixtures are generated by generate_fixtures.py which connects
  to the cardano-node via the N2C local chain-sync mini-protocol and queries
  Ogmios for block metadata (hash, era, tx count) as the oracle of truth.
  Tests decode the fixture CBOR with our block header decoder and verify
  correctness against the Ogmios-reported values.

Spec references:
  - shelley.cddl: block, header, header_body
  - babbage.cddl: header_body (vrf_result replaces nonce_vrf + leader_vrf)
  - Hard-fork combinator era tags: 0=Byron main, 1=Byron EBB, 2=Shelley,
    3=Allegra, 4=Mary, 5=Alonzo, 6=Babbage, 7=Conway

Test specifications consulted (from test_specifications table):
  - test_byron_block_roundtrip_main / test_byron_block_roundtrip_ebb
  - test_blockproof_cbor_roundtrip
  - test_send_uses_serialised_receive_deserialises
  - test_constr_encoding_matches_haskell_golden_vectors
"""

